<h3>¿QUÉ ES UNA COOKIE?</h3><br>
<p>Una cookie es un fichero que se descarga en su ordenador al acceder a determinadas páginas web. Las cookies permiten a una<br>
página web, entre otras cosas, almacenar y recuperar información sobre los hábitos de navegación de un usuario o de su equipo y,<br>
dependiendo de la información que contengan y de la forma en que utilice su equipo, pueden utilizarse para reconocer al usuario.<br>
</p><br>
<h3>¿QUÉ TIPOS DE COOKIES UTILIZA INFOCE?</h3>
    <p>
    Según el plazo de tiempo que permanecen activas las cookies pueden ser:<br>
	-Cookies de sesión: diseñadas para recabar y almacenar datos mientras el usuario accede a una página web. Se suelen emplear<br>
	para almacenar información que sólo interesa conservar para la prestación del servicio solicitado por el usuario en una sola<br>
	ocasión (por ejemplo, una lista de productos adquiridos).<br>
	-Cookies persistentes: Son un tipo de cookies por las que los datos siguen almacenados en el terminal y puede accederse a<br>
	ellos y ser tratados durante un periodo definido. Tienen fecha de borrado. Se utilizan por ejemplo en el proceso de compra o<br>
	registro para evitar tener que introducir nuestros datos constantemente.<br>
	Según quien sea la entidad que gestione el equipo o dominio desde donde se envían las cookies y trate los datos que se obtengan, podemos distinguir:<br>
	-Cookies propias: Son aquellas que se envían al dispositivo del usuario gestionadas exclusivamente por nosotros para el mejor<br>
	funcionamiento del sitio.<br>
	-Cookies de terceros: Son aquellas que se envían al dispositivo del usuario desde un equipo o dominio que no es gestionado<br>
	por nosotros sino por otra entidad, que tratará los datos obtenidos.<br>
    </p><br>
